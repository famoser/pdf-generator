namespace Famoser\PdfGenerator\Font\Backend\File\Traits;

trait RawContent
{
    /**
     * raw byte data.
     */
    private string $content;

    public function getContent(): string
    {
        return $this->content;
    }

    public function setContent(string $content): void
    {
        $this->content = $content;
    }
}
