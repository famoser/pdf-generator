namespace PdfGenerator\Font\Frontend;

class LongDateTime
{
    /**
     * number of seconds since 1. Jan 1904.
     *
     * @var int
     */
    private $value;

    /**
     * LongDateTime constructor.
     *
     * @param int $value
     */
    public function __construct(int $value)
    {
        $this->value = $value;
    }

    /**
     * @return int
     */
    public function getValue(): int
    {
        return $this->value;
    }
}
